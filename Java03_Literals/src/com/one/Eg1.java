package com.one;

public class Eg1 {

	public static void main(String[] args) {
		//Integer Literals
		int a =10;
		System.out.println(a);
		// int is a data type;
		//a is a variable;
		//= is a operation;
		//10 is a constant value
		
		
		
		//Floating Point
		
		float f1=10.0f;
		System.out.println(f1);
		
		double d1=10.0d;
		System.out.println(d1);
		
		//Boolean Literals
		
		boolean b1= true;
		System.out.println(b1);
		
		
		//Character Literals
		
		char ch1='A';
		System.out.println(ch1);
		
		
		
		//string Literals
		
		String username ="Rajesh Reddy";
		System.out.println(username);
	}

}
