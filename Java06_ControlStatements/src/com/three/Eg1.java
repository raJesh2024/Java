package com.three;
//transfer statements:
//break and continue
public class Eg1 {


	public static void main(String[] args) {
		for(int i=0;i<=10;i++) {
			if(i==7) {
				break; //stops iteration
			}
			System.out.println(i);
		}
		
		
		for(int i=0;i<=10;i++) {
			if(i==7) {
				continue;//skips iteration
			}
			System.out.println(i);
		

	}

}
}
