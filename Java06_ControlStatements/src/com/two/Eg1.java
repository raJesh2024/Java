package com.two;
//iteration statements:
//for,while,do-while
//for loop
public class Eg1 {

	public static void main(String[] args) {
		// when we know the number of conditions in advance we can use for loop
		for(int i=0; i<=5;i++) {
			System.out.println(i);
		}
		for (int j=0;j<=10;j++) {
			System.out.println(j);
		}
			

	}

}
