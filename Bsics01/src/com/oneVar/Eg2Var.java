package com.oneVar;

public class Eg2Var {
	
	//static variable
	
	static byte b;
	static short s;
	static int i;
	static long l;
	static boolean bb;
	static float f;
	static double d;
	static char ch;
	

	public static void main(String[] args) {
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(bb);
		System.out.println(d);
		System.out.println(f);
		System.out.println(ch);

	}

}
