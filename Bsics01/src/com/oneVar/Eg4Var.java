package com.oneVar;

public class Eg4Var {

	public static void main(String[] args) {
		// local variable
		byte b=10;
		short s=20;
		int i=30;
		long l=40;
		boolean bb=true;
		double d=42.0d;
		float f=120.0f;
		char ch='a';
		
		
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		
		System.out.println(bb);
		System.out.println(d);
		System.out.println(f);
		
		System.out.println(ch);
	}

}
